//! Публичный API редактора Zol.
//!
//! Каждый подмодуль — узкое направление с собственными тестами.
//! Не имеет циклических зависимостей между собой.
//!
//! # Подмодули
//!
//! | Модуль    | Назначение                            |
//! |-----------|---------------------------------------|
//! | `doc`     | Создание документа, чтение текста     |
//! | `cursor`  | Навигация курсора и прямые доступоры  |
//! | `text`    | Редактирование текста (вставка/удал.) |
//! | `file`    | Сохранение и загрузка файлов          |
//! | `editor`  | Режимы редактора (Preview/Source)     |
//! | `zoll`    | Парсинг .zoll-разметки                |
//! | `theme`   | Темы оформления                       |
//! | `gui`     | GUI-ручки (iced/egui)                 |

pub mod doc;
pub mod cursor;
pub mod text;
pub mod file;
pub mod editor;
pub mod zoll;
